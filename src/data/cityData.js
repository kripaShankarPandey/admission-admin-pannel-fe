// Helper: Get all states as a flat sorted array
export const getAllStates = () => Object.keys(statesWithCities).sort();

// Helper: Get cities for a given state
export const getCitiesByState = (state) => {
  return (statesWithCities[state] || []).sort();
};

// Helper: Get all cities as flat array with state info
export const getAllCitiesWithState = () => {
  const result = [];
  for (const [state, cities] of Object.entries(statesWithCities)) {
    for (const city of cities) {
      result.push({ city, state });
    }
  }
  return result.sort((a, b) => a.city.localeCompare(b.city));
};
